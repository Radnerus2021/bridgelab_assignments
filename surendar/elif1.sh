then
	echo "Number in One"
elif [ $n -eq "2" ]
then 
	echo "Number is Two"
elif [ $n -eq "3" ]
then
	echo "Number is Three"
elif [ $n -eq "4" ]
then
	echo "Number is Four"
elif [ $n -eq "5" ]
then
	echo "Number is five"
else
	echo "Enter the number"
fi
